<br>
<h4 align="center">CONDITIONS FOR DONATION OF BLOOD</h4>
<br>
<ol>
<li>The donor shall be in the age group of 18 to 55 years.</li>
<li>The donor shall not be less than 45 kilograms.</li>
<li>Temperature and Pulse of the donor shall be normal.</li>
<li>The systolic and diastolic blood pressures are within normal limits without medication.</li>
<li>Haemoglobin which shall not be less than 12.5 grams.</li>
<li>The donor shall be free from acute respiratory diseases.</li>
<li>The donor shall be free from any skin diseases at the site of phlebotomy.</li>
<li>The donor shall be free from any disease transmissible by blood transfusion,
  insofar as can be determined by history and examination indicated above.</li>
<li>The arms and forearms of the donor shall be free from skin punctures or
   scars indicative of professional blood donors or addiction of self injected narcotics.</li>
</ol>
